// Bengali Number Conversion helper
export function toBanglaDigits(num: number | string, useBangla: boolean = true): string {
  const numStr = String(num);
  if (!useBangla) {
    return numStr;
  }
  const banglaDigits: { [key: string]: string } = {
    '0': '০', '1': '১', '2': '২', '3': '৩', '4': '৪',
    '5': '৫', '6': '৬', '7': '৭', '8': '৮', '9': '৯'
  };
  return numStr.replace(/[0-9]/g, digit => banglaDigits[digit]);
}

export function formatCurrency(amount: number, useBangla: boolean = true, currencySymbol: string = '৳'): string {
  // Format numbers nicely, e.g., 12,345
  const formattedEn = amount.toLocaleString('bn-BD', { minimumFractionDigits: 0, maximumFractionDigits: 2 });
  if (useBangla) {
    return `${currencySymbol} ${toBanglaDigits(formattedEn, true)}`;
  }
  return `${currencySymbol} ${formattedEn}`;
}

